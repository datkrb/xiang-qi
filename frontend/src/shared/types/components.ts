// Shared component type definitions

/**
 * Size variant for UI components
 */
export type ComponentSize = "sm" | "md" | "lg";

/**
 * Variant for Button-like components
 */
export type ButtonVariant = "primary" | "secondary" | "ghost";

/**
 * Status variant for Badge and status indicators
 */
export type StatusVariant = "success" | "error" | "warning" | "info";

/**
 * Text heading levels
 */
export type HeadingLevel = "h1" | "h2" | "h3" | "h4" | "h5" | "h6";

/**
 * Text variant for body, labels, etc.
 */
export type TextVariant = "body" | "small" | "label" | "caption";

/**
 * Card variant for different visual styles
 */
export type CardVariant = "elevated" | "outlined";

/**
 * Validation state for input fields
 */
export type ValidationState = "default" | "error" | "success" | "warning";

/**
 * Modal size variant
 */
export type ModalSize = "sm" | "md" | "lg";
